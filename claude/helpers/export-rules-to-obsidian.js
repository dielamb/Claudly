#!/usr/bin/env node
'use strict';
/**
 * Exports rules.json to Obsidian vault as a readable .md file
 * so graphify can include behavioral rules in the knowledge graph.
 * Run before graphify monthly cron.
 *
 * Per-rule wikilinks: if a rule object carries a `wikilinks` array
 * (written by dream-worker at rule-creation time), those links are
 * appended to the rule's bullet point as "Related: [[A]] · [[B]]".
 * Rules that have no wikilinks field, or an empty array, are rendered
 * without a Related line — no blank noise.
 *
 * Stale-link detection: titles are validated against the current vault
 * state. Mismatches are written to wikilink-warnings.txt next to this
 * script and printed to stderr, but do NOT abort the export.
 */

const fs = require('fs');
const path = require('path');
const { loadVaultNoteTitles } = require('./vault-utils');

const HOME = process.env.HOME || '__HOME__';
const RULES_PATH = path.join(HOME, '.claude', 'learning', 'rules.json');
const OUT_PATH = path.join(HOME, 'Desktop', 'Labirynt', '3 Atlas', 'Tools', 'AI Rules - Dreamer Learned.md');

/**
 * Validates each rule's wikilinks array against current vault note titles.
 * Returns an array of warning strings for stale (non-existent) links.
 *
 * @param {object[]} rules
 * @param {string[]} currentTitles
 * @returns {string[]}
 */
function validateWikilinks(rules, currentTitles) {
  const titleSet = new Set(currentTitles);
  const warnings = [];
  for (const rule of rules) {
    for (const link of (rule.wikilinks || [])) {
      // Strip [[ and ]] to get the bare title for lookup.
      const title = link.replace(/^\[\[/, '').replace(/\]\]$/, '');
      if (!titleSet.has(title)) {
        warnings.push(`WARN: rule ${rule.id} has stale link ${link} (note not found in vault)`);
      }
    }
  }
  return warnings;
}

function run() {
  if (!fs.existsSync(RULES_PATH)) {
    console.error('[export-rules] rules.json not found, skipping');
    process.exit(0);
  }

  let rules;
  try {
    rules = JSON.parse(fs.readFileSync(RULES_PATH, 'utf8'));
  } catch (e) {
    console.error('[export-rules] Failed to parse rules.json:', e.message);
    process.exit(0);
  }

  // Validate per-rule wikilinks against current vault state.
  // Stale links are flagged but do not abort the export.
  const currentTitles = loadVaultNoteTitles();
  const warnings = validateWikilinks(rules, currentTitles);
  if (warnings.length > 0) {
    console.warn(warnings.join('\n'));
    const warningsPath = path.join(__dirname, 'wikilink-warnings.txt');
    fs.writeFileSync(warningsPath, warnings.join('\n') + '\n');
    console.warn(`[export-rules] Stale wikilinks written to ${warningsPath}`);
  }

  const byOrigin = {};
  for (const r of rules) {
    if (r.disabled) continue;
    const origin = r.origin || 'manual';
    if (!byOrigin[origin]) byOrigin[origin] = [];
    byOrigin[origin].push(r);
  }

  const lines = [
    '---',
    'type: reference',
    `updated: ${new Date().toISOString().split('T')[0]}`,
    'tags: [AI, dreamer, rules, autogenerated]',
    'source: ~/.claude/learning/rules.json',
    '---',
    '',
    '# AI Rules — Dreamer Learned',
    '',
    `> Auto-exported from \`~/.claude/learning/rules.json\` (${rules.filter(r => !r.disabled).length} active rules).`,
    '> Do not edit manually — regenerated before each graphify run.',
    '',
    '## Powiązane',
    '',
    '[[GAN Loop - Generator-Evaluator pipeline]] [[Janitor Crew System]] [[RuFlo Setup]] [[GSD visual verification]] [[Dream Worker Meta-Session Bug]]',
    '',
  ];

  for (const [origin, group] of Object.entries(byOrigin)) {
    lines.push(`## ${origin} (${group.length})`);
    lines.push('');
    for (const r of group.sort((a, b) => (b.confidence || 7) - (a.confidence || 7))) {
      const conf = r.confidence || 7;
      const trigger = r.trigger?.nl ? ` _(when: ${r.trigger.nl})_` : '';
      const tags = r.tags?.length ? ` \`${r.tags.join('`, `')}\`` : '';
      // Per-rule wikilinks written by dream-worker at creation time.
      // Rules without the field, or with an empty array, get no Related line.
      const perRuleLinks = Array.isArray(r.wikilinks) && r.wikilinks.length > 0
        ? ` — Related: ${r.wikilinks.join(' · ')}`
        : '';
      lines.push(`- **[${conf}/10]** ${r.rule}${trigger}${tags}${perRuleLinks}`);
    }
    lines.push('');
  }

  lines.push('## Stats');
  lines.push('');
  lines.push(`- Total: ${rules.filter(r => !r.disabled).length} active rules`);
  lines.push(`- With triggers: ${rules.filter(r => !r.disabled && r.trigger?.match?.length > 0).length}`);
  lines.push(`- Avg confidence: ${(rules.filter(r => !r.disabled).reduce((s, r) => s + (r.confidence || 7), 0) / Math.max(1, rules.filter(r => !r.disabled).length)).toFixed(1)}`);

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, lines.join('\n') + '\n');
  console.log(`[export-rules] Wrote ${rules.filter(r => !r.disabled).length} rules to ${OUT_PATH}`);
}

run();
